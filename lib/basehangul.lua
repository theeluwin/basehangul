local function int2bin(data, luka)
	local bin = ""
	for i = 1, luka do
		if data == 0 then
			bin = "0" .. bin
		else
			bin = "" .. (data % 2) .. bin
			data = math.floor(data / 2)
		end
	end
	return bin
end

local function asc2bin(data)
	local bin = ""
	local asc_list = {string.byte(data, 0, -1)}
	for i = 1, #asc_list do
		bin = bin .. int2bin(asc_list[i], 8)
	end
	while string.len(bin) % 40 ~= 0 do
		bin = bin .. "0"
	end
	return bin
end

local function bin2hangul(data)
	local hangul = ""
	local data_length = string.len(data) / 10
	for i = 1, data_length do
		local index = tonumber(string.sub(data, (i - 1) * 10 + 1, i * 10), 2)
		if index == 0 then
			if i == data_length then
				hangul = hangul .. "빎"
			else
				hangul = hangul .. "흐"
			end
		else
			hangul = hangul .. string.sub(ksc, index * 3 + 1, (index + 1) * 3)
		end
	end
	return hangul
end

local function hangul2bin(data)
	local bin = ""
	for i = 1, string.len(data) / 3 do
		local hangul = string.sub(data, (i - 1) * 3 + 1, i * 3)
		local index = string.find(ksc, hangul)
		local chunk
		if index == nil or index == 3073 then
			chunk = "0000000000"
		else
			chunk = int2bin((index - 1) / 3, 10)
		end
		bin = bin .. chunk
	end
	return bin
end

local function bin2asc(data)
	local asc = ""
	for i = 1, string.len(data) / 8 do
		local dec = tonumber(string.sub(data, (i - 1) * 8 + 1, i * 8), 2)
		asc = string.format("%c", dec) .. asc
	end
	return asc
end

function encode(data)
	return bin2hangul(asc2bin(data))
end

function decode(data)
	return bin2asc(hangul2bin(data))
end
